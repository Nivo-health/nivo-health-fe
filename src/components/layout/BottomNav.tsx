export default function BottomNav() {
  const location = useLocation();

  const isActive = (path: string) => {
    if (path === '/') {
      return location.pathname === '/';
    }
    return location.pathname.startsWith(path);
  };

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-white border-t border-teal-200 shadow-lg md:hidden">
      <div className="flex items-center justify-around h-16 px-2">
        {navItems.map((item) => (
          <Link
            key={item.path}
            to={item.path}
            className={cn(
              'flex flex-col items-center justify-center flex-1 h-full transition-colors',
              isActive(item.path)
                ? 'text-teal-600'
                : 'text-gray-500 hover:text-teal-600',
            )}
          >
            <div
              className={cn(
                'flex items-center justify-center mb-1',
                isActive(item.path) && 'text-teal-600',
              )}
            >
              {item.icon}
            </div>
            <span className="text-xs font-medium">{item.name}</span>
            {isActive(item.path) && (
              <div className="absolute bottom-0 left-1/2 transform -translate-x-1/2 w-12 h-1 bg-teal-600 rounded-t-full" />
            )}
          </Link>
        ))}
      </div>
    </nav>
  );
}
